class Client {
  final String id;
  final String nombre;
  final String nit;
  final String direccion;
  final String telefono;
  final String email;
  final String ciudad;
  final String zona;
  final String tipoCliente;

  Client({
    required this.id,
    required this.nombre,
    required this.nit,
    required this.direccion,
    required this.telefono,
    required this.email,
    required this.ciudad,
    required this.zona,
    required this.tipoCliente,
  });
}
